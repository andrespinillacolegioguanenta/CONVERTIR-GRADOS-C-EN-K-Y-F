# Programa para pasar de C a F,K

print("--------------------------------------------------")
print("------------conversor de temperatura--------------")
print("pasar de grado Celsius agrados Fharenheit y Kelvin")
print("--------------------------------------------------")

#----------------------------------------------------------
# INPUT
#----------------------------------------------------------

C=int(input("Digita la temperatura en grados Celsius: "))

#----------------------------------------------------------
# PROCESING 
#----------------------------------------------------------

F=(9/5*C) + 32
K=C + 273.15

#----------------------------------------------------------
# OUTPUT
#----------------------------------------------------------

print("--------------------------------------------------")
print("-------------Resultado de conversión--------------")
print("--------------------------------------------------")
print("Temperatura °C convertida a °F: "+str(F))
print("Temperatura °C convertida a °K: "+str(K))
print("--------------------------------------------------")
